def what_is_my_sign(day, month):
    ALL_MONTHE_AND_SIGNS = [(119, "Козирог"), (218, "Водолей"),
                            (320, "Риби"), (420, "Овен"), (520, "Телец"),
                            (620, "Близнаци"), (721, "Рак"), (822, "Лъв"),
                            (922, "Дева"), (1022, "Везни"), (1121, "Скорпион"),
                            (1221, "Стрелец"), (1231, "Козирог")]

    for i in ALL_MONTHE_AND_SIGNS:
        if month * 100 + day <= i[0]:
            return i[1]
